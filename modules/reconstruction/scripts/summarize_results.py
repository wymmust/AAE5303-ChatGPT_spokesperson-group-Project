#!/usr/bin/env python3
"""Summarize reconstruction outputs into JSON and Markdown reports."""

from __future__ import annotations

import argparse
import json
import re
from pathlib import Path


def main() -> int:
    args = parse_args()
    summary = collect_summary(args.results_dir)
    write_json(summary, args.json_output)
    write_markdown(summary, args.markdown_output)
    print(f"Wrote {args.json_output}")
    print(f"Wrote {args.markdown_output}")
    return 0


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--results-dir",
        type=Path,
        default=Path(__file__).resolve().parents[1] / "results",
        help="Directory containing reconstruction outputs.",
    )
    parser.add_argument(
        "--json-output",
        type=Path,
        default=Path(__file__).resolve().parents[1] / "results" / "reconstruction_summary.json",
        help="Path to the generated JSON summary.",
    )
    parser.add_argument(
        "--markdown-output",
        type=Path,
        default=Path(__file__).resolve().parents[1] / "results" / "RESULT_SUMMARY.md",
        help="Path to the generated Markdown summary.",
    )
    return parser.parse_args()


def collect_summary(results_dir: Path) -> dict:
    outputs = []
    for path in sorted(results_dir.rglob("*")):
        if path.suffix.lower() not in {".ply", ".splat"}:
            continue
        outputs.append(read_output_metadata(results_dir, path))

    cameras = {}
    for path in sorted(results_dir.rglob("cameras.json")):
        cameras[str(path.relative_to(results_dir))] = count_cameras(path)

    return {
        "results_dir": str(results_dir),
        "output_count": len(outputs),
        "outputs": outputs,
        "camera_sets": cameras,
    }


def read_output_metadata(results_dir: Path, path: Path) -> dict:
    info = {
        "relative_path": str(path.relative_to(results_dir)),
        "file_name": path.name,
        "file_type": path.suffix.lower().lstrip("."),
        "size_bytes": path.stat().st_size,
        "iteration": None,
        "vertex_count": None,
    }

    if path.suffix.lower() == ".ply":
        iteration, vertex_count = read_ply_header(path)
        info["iteration"] = iteration
        info["vertex_count"] = vertex_count

    return info


def read_ply_header(path: Path) -> tuple[int | None, int | None]:
    with path.open("rb") as handle:
        header = handle.read(4096).decode("utf-8", errors="ignore")

    iteration_match = re.search(r"comment Generated by opensplat at iteration (\d+)", header)
    vertex_match = re.search(r"element vertex (\d+)", header)

    iteration = int(iteration_match.group(1)) if iteration_match else None
    vertex_count = int(vertex_match.group(1)) if vertex_match else None
    return iteration, vertex_count


def count_cameras(path: Path) -> int | None:
    try:
        with path.open("r", encoding="utf-8") as handle:
            data = json.load(handle)
    except (OSError, json.JSONDecodeError):
        return None

    if isinstance(data, list):
        return len(data)
    return None


def write_json(summary: dict, output_path: Path) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with output_path.open("w", encoding="utf-8") as handle:
        json.dump(summary, handle, indent=2)


def write_markdown(summary: dict, output_path: Path) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# Reconstruction Result Summary",
        "",
        f"- Total outputs: {summary['output_count']}",
        f"- Results directory: `{summary['results_dir']}`",
        "",
        "## Outputs",
        "",
        "| File | Type | Size (MB) | Iteration | Vertices |",
        "| --- | --- | ---: | ---: | ---: |",
    ]

    for item in summary["outputs"]:
        size_mb = item["size_bytes"] / (1024 * 1024)
        iteration = item["iteration"] if item["iteration"] is not None else "-"
        vertices = item["vertex_count"] if item["vertex_count"] is not None else "-"
        lines.append(
            f"| `{item['relative_path']}` | {item['file_type']} | {size_mb:.2f} | {iteration} | {vertices} |"
        )

    lines.extend(
        [
            "",
            "## Camera Files",
            "",
            "| File | Camera Count |",
            "| --- | ---: |",
        ]
    )

    for relative_path, camera_count in summary["camera_sets"].items():
        value = camera_count if camera_count is not None else "-"
        lines.append(f"| `{relative_path}` | {value} |")

    lines.extend(
        [
            "",
            "## Recommended Files For Presentation",
            "",
            "- `amtown02_quality_balanced_6000.ply`: the best high-quality scene already available in the repository.",
            "- `amtown02_gpu_final_2000.splat`: a compact GPU result suitable for lightweight viewers.",
            "- `amtown02_vo_safe/scene_gpu_safe_n500_d3.ply`: the newly generated VO-driven reconstruction output.",
        ]
    )

    with output_path.open("w", encoding="utf-8") as handle:
        handle.write("\n".join(lines) + "\n")


if __name__ == "__main__":
    raise SystemExit(main())
