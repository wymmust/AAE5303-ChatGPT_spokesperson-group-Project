#!/usr/bin/env python3
"""Summarize reconstruction outputs into JSON and Markdown reports."""

from __future__ import annotations

import argparse
import json
import re
from pathlib import Path

CORRECTED_RESULT_PREFIXES = {
    "amtown02_vo_amtown02_safe",
    "amtown02_vo_amtown02_10000",
    "amtown02_vo_amtown02_20000",
    "amtown02_vo_amtown02_30000",
    "amtown02_vo_amtown02_35000",
    "amtown02_vo_amtown02_40000",
    "amtown02_vo_amtown02_aggressive_hq2_35000",
    "amtown02_colmap_baseline_hq2_35000",
}


def main() -> int:
    args = parse_args()
    summary = collect_summary(args.results_dir)
    write_json(summary, args.json_output)
    write_markdown(summary, args.markdown_output)
    print(f"Wrote {args.json_output}")
    print(f"Wrote {args.markdown_output}")
    return 0


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--results-dir",
        type=Path,
        default=Path(__file__).resolve().parents[1] / "results",
        help="Directory containing reconstruction outputs.",
    )
    parser.add_argument(
        "--json-output",
        type=Path,
        default=Path(__file__).resolve().parents[1] / "results" / "reconstruction_summary.json",
        help="Path to the generated JSON summary.",
    )
    parser.add_argument(
        "--markdown-output",
        type=Path,
        default=Path(__file__).resolve().parents[1] / "results" / "RESULT_SUMMARY.md",
        help="Path to the generated Markdown summary.",
    )
    return parser.parse_args()


def collect_summary(results_dir: Path) -> dict:
    results_dir = results_dir.resolve()
    module_root = Path(__file__).resolve().parents[1]
    try:
        results_dir_display = str(results_dir.relative_to(module_root))
    except ValueError:
        results_dir_display = str(results_dir)
    outputs = []
    for path in sorted(results_dir.rglob("*")):
        if path.suffix.lower() not in {".ply", ".splat"}:
            continue
        if not should_include_path(results_dir, path):
            continue
        if _is_intermediate_checkpoint_ply(path):
            continue
        outputs.append(read_output_metadata(results_dir, path))

    cameras = {}
    for path in sorted(results_dir.rglob("cameras.json")):
        if not should_include_path(results_dir, path):
            continue
        cameras[str(path.relative_to(results_dir))] = count_cameras(path)

    return {
        "results_dir": results_dir_display,
        "output_count": len(outputs),
        "outputs": outputs,
        "camera_sets": cameras,
    }


def should_include_path(results_dir: Path, path: Path) -> bool:
    relative_parts = path.relative_to(results_dir).parts
    if not relative_parts:
        return False
    return relative_parts[0] in CORRECTED_RESULT_PREFIXES


def read_output_metadata(results_dir: Path, path: Path) -> dict:
    info = {
        "relative_path": str(path.relative_to(results_dir)),
        "file_name": path.name,
        "file_type": path.suffix.lower().lstrip("."),
        "size_bytes": path.stat().st_size,
        "iteration": None,
        "vertex_count": None,
    }

    if path.suffix.lower() == ".ply":
        iteration, vertex_count = read_ply_header(path)
        info["iteration"] = iteration
        info["vertex_count"] = vertex_count

    return info


def read_ply_header(path: Path) -> tuple[int | None, int | None]:
    with path.open("rb") as handle:
        header = handle.read(4096).decode("utf-8", errors="ignore")

    iteration_match = re.search(r"comment Generated by opensplat at iteration (\d+)", header)
    vertex_match = re.search(r"element vertex (\d+)", header)

    iteration = int(iteration_match.group(1)) if iteration_match else None
    vertex_count = int(vertex_match.group(1)) if vertex_match else None
    return iteration, vertex_count


def count_cameras(path: Path) -> int | None:
    try:
        with path.open("r", encoding="utf-8") as handle:
            data = json.load(handle)
    except (OSError, json.JSONDecodeError):
        return None

    if isinstance(data, list):
        return len(data)
    return None


def write_json(summary: dict, output_path: Path) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with output_path.open("w", encoding="utf-8") as handle:
        json.dump(summary, handle, indent=2)


def write_markdown(summary: dict, output_path: Path) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# Reconstruction Result Summary",
        "",
        f"- Total outputs: {summary['output_count']}",
        f"- Results directory: `{summary['results_dir']}`",
        "",
        "## Outputs",
        "",
        "| File | Type | Size (MB) | Iteration | Vertices |",
        "| --- | --- | ---: | ---: | ---: |",
    ]

    for item in summary["outputs"]:
        size_mb = item["size_bytes"] / (1024 * 1024)
        iteration = item["iteration"] if item["iteration"] is not None else "-"
        vertices = item["vertex_count"] if item["vertex_count"] is not None else "-"
        lines.append(
            f"| `{item['relative_path']}` | {item['file_type']} | {size_mb:.2f} | {iteration} | {vertices} |"
        )

    lines.extend(
        [
            "",
            "## Camera Files",
            "",
            "| File | Camera Count |",
            "| --- | ---: |",
        ]
    )

    for relative_path, camera_count in summary["camera_sets"].items():
        value = camera_count if camera_count is not None else "-"
        lines.append(f"| `{relative_path}` | {value} |")

    lines.extend(
        [
            "",
            "## Recommended Files For Presentation",
            "",
            "- `amtown02_vo_amtown02_aggressive_hq2_35000/scene_aggressive_hq2_n35000_d2_sh3.ply`: the selected VO-connected AMtown02 result (aggressive HQ2 OpenSplat profile).",
            "- `amtown02_vo_amtown02_35000/scene_gpu_safe_n35000_d3.ply`: the best conservative-profile rerun for browser viewing and size tradeoffs.",
            "- `amtown02_vo_amtown02_40000/scene_gpu_safe_n40000_d3.ply`: the longest conservative rerun (viewer compatibility varies).",
            "- `amtown02_colmap_baseline_hq2_35000/scene_colmap_baseline_hq2_n35000_d2_sh3.ply`: **V7** — aggressive HQ2 with **changed** COLMAP input (`colmap_from_amtown02`, **292** registered cameras in this snapshot).",
            "- `amtown02_vo_amtown02_safe/scene_gpu_safe_n500_d3.ply`: smoke test for wiring and CUDA.",
            "",
            "## Notes",
            "",
            "- Conservative VO reruns use `baseline/colmap_from_vo_amtown02` with the stable low-SH / heavy-downscale preset.",
            "- Aggressive HQ2 uses the **same** `colmap_from_vo_amtown02` registration (**622** cameras) as the conservative line; only OpenSplat hyperparameters differ.",
            "- FAQ: OpenSplat only consumes cameras in `images.bin`; extra PNG files on disk are ignored until registered in COLMAP (this is not what changed between conservative and aggressive VO runs).",
        ]
    )

    with output_path.open("w", encoding="utf-8") as handle:
        handle.write("\n".join(lines) + "\n")


def _is_intermediate_checkpoint_ply(path: Path) -> bool:
    """True for OpenSplat periodic saves like *_5000.ply (not the final scene file)."""
    if path.suffix.lower() != ".ply":
        return False
    return bool(re.fullmatch(r".+_\d{4,}\.ply", path.name))


if __name__ == "__main__":
    raise SystemExit(main())
